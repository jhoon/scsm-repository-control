package philips.scsm.data;

/**
 *
 * @author pablo.sierralta
 */
public class Solution {

    private String id;
    private String description;
    private String asigneed;
    private String status;
    private String resolution;
    private String opSys;
    private String priority;
    private String severity;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the asigneed
     */
    public String getAsigneed() {
        return asigneed;
    }

    /**
     * @param asigneed the asigneed to set
     */
    public void setAsigneed(String asigneed) {
        this.asigneed = asigneed;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the resolution
     */
    public String getResolution() {
        return resolution;
    }

    /**
     * @param resolution the resolution to set
     */
    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    /**
     * @return the opSys
     */
    public String getOpSys() {
        return opSys;
    }

    /**
     * @param opSys the opSys to set
     */
    public void setOpSys(String opSys) {
        this.opSys = opSys;
    }

    /**
     * @return the priority
     */
    public String getPriority() {
        return priority;
    }

    /**
     * @param priority the priority to set
     */
    public void setPriority(String priority) {
        this.priority = priority;
    }

    /**
     * @return the severity
     */
    public String getSeverity() {
        return severity;
    }

    /**
     * @param severity the severity to set
     */
    public void setSeverity(String severity) {
        this.severity = severity;
    }
}
